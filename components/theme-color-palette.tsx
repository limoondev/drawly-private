export function useThemeColorPalette(): string[] {
  const { currentTheme } = useThemeEvent()
  return THEME_PALETTES[currentTheme || "galaxy"] || THEME_PALETTES.galaxy
}

export function ThemeColorPalettePreview() {
  const palette = useThemeColorPalette()

  return (
    <div className="flex flex-wrap gap-1">
      {palette.slice(0, 12).map((color, i) => (
        <div key={i} className="w-5 h-5 rounded border border-white/20" style={{ backgroundColor: color }} />
      ))}
    </div>
  )
}
